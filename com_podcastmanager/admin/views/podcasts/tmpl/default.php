				<td class="center nowrap">
					<?php if ($item->language=='*') {
						echo JText::alt('JALL', 'language');
					} else {
						echo $item->language_title ? $this->escape($item->language_title) : JText::_('JUNDEFINED');
					} ?>
				</td>
				<td class="center">
					<?php echo (int) $item->id; ?>
				</td>
			</tr>
			<?php endforeach; ?>
